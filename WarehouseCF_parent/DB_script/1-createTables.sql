create TABLE W_F_COLOR_BRANCH
(
COLOR_BRANCH_ID int(20) NOT NULL AUTO_INCREMENT COMMENT '子类颜色的编号',
COLOR_BRANCH_NAME VARCHAR(20) NULL COMMENT '子类颜色的名称',
COLOR_BRANCH_PARENT VARCHAR(2) NULL COMMENT '父类颜色的名称',
