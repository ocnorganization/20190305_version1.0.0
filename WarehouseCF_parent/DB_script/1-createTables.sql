--创建员工管理数据库
create TABLE W_STAFF
(
STAFF_ID VARCHAR(20) NOT NULL,
STAFF_NAME VARCHAR(20) NOT NULL,
STAFF_GENDER enum(''男'',''女''),
STAFF_POSITION VARCHAR(50),
STAFF_POWER INT(2) NOT NULL,
PRIMARY KEY(STAFF_ID)
);
